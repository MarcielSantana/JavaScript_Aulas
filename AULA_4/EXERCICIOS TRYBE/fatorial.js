// 1 - Criar a variável fatorial começando em 1.

// 2 - Usar o for para contar de 10 até 1 (diminuindo).

// 3- Multiplicar o fatorial pelo número atual (index) em cada volta.

// 4 - Mostrar o resultado final com console.log(fatorial) fora do for.


let fatorial = 1

for (let index = 10; index >= 1; index --){
    fatorial = fatorial * index 
}
console.log(fatorial);

